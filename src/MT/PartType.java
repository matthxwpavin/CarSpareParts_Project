    private void cbPartGroupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbPartGroupActionPerformed
        // TODO add your handling code here:
        if (cbPartGroup.getSelectedIndex() == 0) {
            return;
        }
        String GroupName = cbPartGroup.getSelectedItem().toString();
        int index = 0;

        for (String str : Name) {
            if (GroupName.equals(str)) {

                break;

            }
            index++;

        }
        int partGroupID = ID[index];

        conn.connect();

        ArrayList<ProductType> ptlist = conn.QueryPartTypeByPartGroupID(partGroupID);

        conn.disconnect();
        modelTable = new productTypeTableModel();
        modelTable.addAllData(ptlist);
        partTypeTable.setModel(modelTable);


    }//GEN-LAST:event_cbPartGroupActionPerformed

