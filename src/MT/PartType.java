    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if (cbPartGroup.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "กรุณาเลือกกลุ่มสินค้า");
            return;
        }
        if (tfPartType.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "กรุณาเพิ่มประเทย่อย");
            return;
        }
        String nameGroup = cbPartGroup.getSelectedItem().toString();
        String nameType = tfPartType.getText();

        int index = 0;
        for (String name : Name) {
            if (nameGroup.equals(name)) {

                break;
            }
            index++;
        }
        priKey = ID[index];

        ProductType pt = new ProductType();

        conn.connect();

        int nextID = conn.QueryMaxIDPartType() + 1;
        pt.setID(nextID);
        pt.setName(nameType);
        pt.setPartGroupID(priKey);
        pt.setNameGroup(nameGroup);

        conn.insertMappingPartType(pt);

        tfPartType.setText("");

        conn.disconnect();
        this.setVisible(false);

    }//GEN-LAST:event_btnAddActionPerformed

